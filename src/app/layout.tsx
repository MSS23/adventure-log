export const viewport = {
  width: 'device-width',
  initialScale: 1,
  // Pinch zoom stays enabled (WCAG 1.4.4; this is a photo app — users must be
  // able to zoom photos). Accidental double-tap zoom on controls is prevented
  // by `touch-action: manipulation` on interactive elements instead of a
  // blanket zoom lockout. The 3D globe's in-canvas zoom (OrbitControls) calls
  // preventDefault on its gestures, so it is unaffected.
  maximumScale: 5,
  userScalable: true,
  viewportFit: 'cover',
  // Light-only app: pin the color scheme so form controls / browser chrome
  // don't render in dark on a dark-mode device.
  colorScheme: 'light',
  themeColor: '#F7F9FB'
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              (function() {
                // Adventure Log is light-only. Force light before first paint so
                // no stored/system preference can dark-flash the app.
                try {
                  var root = document.documentElement;
                  root.classList.remove('dark');
                  root.classList.add('light');
                  root.setAttribute('data-theme', 'light');
                  localStorage.setItem('adventure-log-theme', 'light');
                } catch(e) {}
              })();
            `,
          }}
        />
      </head>
      <body
        className={`${dmSans.variable} ${playfair.variable} ${geistMono.variable} antialiased`}
        suppressHydrationWarning
      >
        <a
          href="#main-content"
          className="sr-only focus:not-sr-only focus:fixed focus:top-3 focus:left-3 focus:z-[100] focus:bg-olive-700 focus:text-white focus:rounded-md focus:px-3 focus:py-2 focus:shadow-lg"
        >
          Skip to main content
        </a>
          <ErrorBoundary>
            <QueryProvider>
              <ThemeProvider>
                <GlobalMotionConfig>
                  <ToastProvider>
                    <ConditionalAuthProvider>
                      <ServiceWorkerRegistration />
                      <NativeNavigationAdapter />
                      <NativeAppShell />
                      <main id="main-content">{children}</main>
                      <AnalyticsConsent />
                    </ConditionalAuthProvider>
                  </ToastProvider>
                  <ThemedToaster />
                </GlobalMotionConfig>
              </ThemeProvider>
            </QueryProvider>
          </ErrorBoundary>
      </body>
    </html>
  );
}
