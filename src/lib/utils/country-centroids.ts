/**
 * Approximate centroid for an ISO2 country code, or null if unknown.
 * Case-insensitive.
 */
export function getCountryCentroid(code: string | null | undefined): { lat: number; lng: number } | null {
  if (!code) return null
  const entry = COUNTRY_CENTROIDS[code.trim().toUpperCase()]
  return entry ? { lat: entry[0], lng: entry[1] } : null
}
