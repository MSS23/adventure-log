-- =============================================================================
-- IMPORTANT SECURITY NOTES
-- =============================================================================

/*
1. NEVER make the bucket public - this would expose all photos directly
2. Always use signed URLs generated by the server for photo access
3. The server validates user permissions before generating signed URLs
4. Direct client access to storage is completely blocked
5. Only the service role (server) can upload/delete files
6. Users access photos through the Next.js API which handles authorization
7. Signed URLs expire after a configured time (1-4 hours typically)

FLOW:
1. User uploads -> Next.js API validates permissions -> Server uploads to Supabase
2. User views photo -> Next.js API validates permissions -> Server generates signed URL
3. Browser displays photo using temporary signed URL
4. Signed URL expires, forcing re-validation for continued access

This ensures:
- No direct access to storage
- All permissions handled by application logic
- Automatic expiration of access tokens
- Complete audit trail in application logs
*/

