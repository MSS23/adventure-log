-- Supabase Storage Security Policies for Adventure Log
-- This file implements Row Level Security (RLS) for the photos bucket
-- Ensures only the server can access storage directly

-- Enable RLS on the storage.objects table (if not already enabled)
ALTER TABLE storage.objects ENABLE ROW LEVEL SECURITY;

-- Drop existing policies if they exist (for redeployment)
DROP POLICY IF EXISTS "Deny all direct access" ON storage.objects;
DROP POLICY IF EXISTS "Allow service role access" ON storage.objects;
DROP POLICY IF EXISTS "Allow authenticated reads via API" ON storage.objects;

-- =============================================================================
-- BUCKET: adventure-photos
-- =============================================================================

-- Policy 1: Deny all public access by default
-- This is the most restrictive policy and applies to all users
CREATE POLICY "Deny all direct access"
ON storage.objects
FOR ALL
TO public
USING (false)
WITH CHECK (false);

-- Policy 2: Allow service role full access
-- Only the server-side service role can manage files directly
CREATE POLICY "Allow service role access"
ON storage.objects
FOR ALL
TO service_role
USING (true)
WITH CHECK (true);

-- Policy 3: Allow authenticated reads via signed URLs
-- This policy allows reading via signed URLs for authenticated users
-- The application handles authorization in the API layer
CREATE POLICY "Allow authenticated reads via API"
ON storage.objects
FOR SELECT
TO authenticated
USING (
  bucket_id = 'adventure-photos' AND
  -- Only allow access to signed URLs (handled by server)
  -- The server validates permissions before generating signed URLs
  auth.role() = 'authenticated'
);

-- =============================================================================
-- BUCKET CONFIGURATION
-- =============================================================================

-- Ensure the adventure-photos bucket exists with correct settings
INSERT INTO storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
